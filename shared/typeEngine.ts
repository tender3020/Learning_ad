export function parseTypeFromAIResponse(response: string): LearningType {
  const trimmed = response.trim();

  const structured = trimmed.match(/([1-7])\s*\|\s*([a-z_]+)\s*\|/i);
  if (structured) {
    const num = parseInt(structured[1], 10);
    const english = structured[2].toLowerCase() as LearningType;
    if (TYPE_ENGLISH[num] === english) return english;
    if (Object.values(TYPE_ENGLISH).includes(english)) return english;
  }

  const englishTypes: LearningType[] = [
    "operation_logic",
    "abstract_logic",
    "network_assoc",
    "model_apply",
    "perception",
    "practical",
    "language",
  ];
  for (const t of englishTypes) {
    if (trimmed.toLowerCase().includes(t)) return t;
  }

  return "abstract_logic";
}

